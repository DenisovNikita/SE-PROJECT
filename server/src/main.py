def sum(a: int, b: int) -> int:
    """

    Args:
        a (): int
        b (): int

    Returns:
        sum of two integers

    """
    return a + b


def main() -> None:
    """

    Returns:
        None

    """
    a = 2
    b = 2
    print(f'sum({a}, {b}) = {sum(a, b)}')


if __name__ == "__main__":
    main()
